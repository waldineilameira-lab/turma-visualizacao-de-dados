# exemplo 1: if simples
idade = 18
if idade >= 18:
    print("Você é maior de idade.")

# Exemplo 2: if-else
nota = 7.5
if nota >= 7.0:
    print("Você foi aprovado.")
else:
    print("Você não foi aprovado.")

# Exemplo 3: if-elif-else (multiplas condições)

nota = 8.5
if nota >= 9.0:
    print("Excelente!")
elif nota >= 7.0:
    print("Bom trabalho!")
elif nota >= 5.0:
    print("Regular.")
else:    
    print("Insuficiente.")