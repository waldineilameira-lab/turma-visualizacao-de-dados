# numeros inteiros positivos ou negativos

idade = 25
temperatura = -10
ano = 2025

print(idade)
print(temperatura)
print(ano)
# numeros inteiros podem ser usados para fazer calculos
soma = idade + ano
print(soma)
# numeros inteiros podem ser usados para fazer comparações
if idade > 18:
    print("Você é maior de idade") 
else:
    print("Você é menor de idade")
# numeros inteiros podem ser usados para fazer contagem
for i in range(5):
    print(i)
# numeros inteiros podem ser usados para fazer indexação
lista = [1, 2, 3, 4, 5]
print(lista[0]) # imprime o primeiro elemento da lista
print(lista[1]) # imprime o segundo elemento da lista
print(lista[2]) # imprime o terceiro elemento da lista
print(lista[3]) # imprime o quarto elemento da lista
print(lista[4]) # imprime o quinto elemento da lista