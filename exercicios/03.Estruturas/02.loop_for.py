# Exemplo 2: Iterar sobre lista

frutas = ["maçã", "banana", "laranja"]
for fruta in frutas:
    print("Eu gosto de", fruta)
    
# Exemplo 3: Range com início e fim
for num in range(1, 11): # 1 até 10 (11 não inclui)
    print(num)